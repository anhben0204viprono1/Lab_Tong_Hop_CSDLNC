CREATE TABLE PHONGBAN (
    MAPHG NUMBER(2) PRIMARY KEY,
    TENPHG NVARCHAR2(30),
    TRPHG CHAR(3), -- trưởng phòng
    NG_NHANCHUC DATE
);
CREATE TABLE NHANVIEN (
    MANV CHAR(3) PRIMARY KEY,
    HONV NVARCHAR2(15),
    TENLOT NVARCHAR2(15),
    TENNV NVARCHAR2(15),
    NGAYSINH DATE,
    DCHI NVARCHAR2(50),
    PHAI NVARCHAR2(3),
    LUONG NUMBER(10,0),
    MA_NQL CHAR(3),
    PHG NUMBER(2),

    CONSTRAINT fk_nv_phg FOREIGN KEY (PHG) REFERENCES PHONGBAN(MAPHG),
    CONSTRAINT fk_nv_nql FOREIGN KEY (MA_NQL) REFERENCES NHANVIEN(MANV)
);
CREATE TABLE DIADIEM_PHG (
    MAPHG NUMBER(2),
    DIADIEM NVARCHAR2(30),

    CONSTRAINT pk_dd PRIMARY KEY (MAPHG, DIADIEM),
    CONSTRAINT fk_dd_phg FOREIGN KEY (MAPHG) REFERENCES PHONGBAN(MAPHG)
);
CREATE TABLE DEAN (
    MADA NUMBER(3) PRIMARY KEY,
    TENDA NVARCHAR2(30),
    DDIEM_DA NVARCHAR2(30),
    PHONG NUMBER(2),

    CONSTRAINT fk_da_phg FOREIGN KEY (PHONG) REFERENCES PHONGBAN(MAPHG)
);
CREATE TABLE PHANCONG (
    MANV CHAR(3),
    MADA NUMBER(3),
    THOIGIAN NUMBER(5,1),

    CONSTRAINT pk_pc PRIMARY KEY (MANV, MADA),
    CONSTRAINT fk_pc_nv FOREIGN KEY (MANV) REFERENCES NHANVIEN(MANV),
    CONSTRAINT fk_pc_da FOREIGN KEY (MADA) REFERENCES DEAN(MADA)
);
CREATE TABLE THANNHAN (
    MANV CHAR(3),
    TENTN NVARCHAR2(30),
    PHAI NVARCHAR2(3),
    NGAYSINH DATE,
    QUANHE NVARCHAR2(20),

    CONSTRAINT pk_tn PRIMARY KEY (MANV, TENTN),
    CONSTRAINT fk_tn_nv FOREIGN KEY (MANV) REFERENCES NHANVIEN(MANV)
);
