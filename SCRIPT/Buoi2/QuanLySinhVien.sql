CREATE TABLE DMKHOA (
    MAKHOA CHAR(2) PRIMARY KEY,
    TENKHOA NVARCHAR2(30) NOT NULL
);

CREATE TABLE DMMH (
    MAMH CHAR(2) PRIMARY KEY,
    TENMH NVARCHAR2(35) NOT NULL,
    SOTIET NUMBER(3)
);

CREATE TABLE DMSV (
    MASV CHAR(3) PRIMARY KEY,
    HOSV NVARCHAR2(30) NOT NULL,
    TENSV NVARCHAR2(10) NOT NULL,
    PHAI NVARCHAR2(3),
    NGAYSINH DATE,
    NOISINH NVARCHAR2(25),
    MAKH CHAR(2),
    HOCBONG NUMBER(10,0),
    CONSTRAINT FK_DMSV_DMKHOA FOREIGN KEY (MAKH) REFERENCES DMKHOA(MAKHOA)
);

CREATE TABLE KETQUA (
    MASV CHAR(3),
    MAMH CHAR(2),
    DIEM NUMBER(4,2), 
    CONSTRAINT PK_KETQUA PRIMARY KEY (MASV, MAMH),
    CONSTRAINT FK_KQ_SV FOREIGN KEY (MASV) REFERENCES DMSV(MASV),
    CONSTRAINT FK_KQ_MH FOREIGN KEY (MAMH) REFERENCES DMMH(MAMH)
);

